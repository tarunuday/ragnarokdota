<p>
<h3>1. General</h3>

<h4>1.1 Administration and Announcements</h4>

<p>
    Everybody who is authorized by Ragnarok to administrate the competition based on its rules and regulations will be called an "admin" in the following, the collectivity of all admins will be called the "administration". A list of the admins can be found <a href="admins.php">here.</a>
</p>

<h4>1.2 Participants and Teams</h4>

    Every real person - unless oficially banned - with a valid steam account is allowed to participate in the competition and will be called "participant" in the following. Participants joining a team in order to fulfill the minimum required amount of team members will be called a "team" in the tourney.

<h4>1.3 Commitments</h4>

1.3.1 By attending the competition participants acknowledge without limitation to comply with the rules and regulations, official announcement, and with the statements and decisions made by the admins.</br>

1.3.2 Every participant acknowledges the right for the administration to modify the rules and regulations for adjustments at any time without notice.</br>

1.3.3 Every participant has to show the needed respect towards admins and other participants. Insults and unfair or disrespectful behaviour towards anyone are not tolerated and will be punished.</br>

1.3.4 Every participant must always have the newest version of Steam and Dota 2 installed and has to check for updates in time before each match. Unless a new version comes out right before the match, patching is not a valid reason to delay it.</br>

1.3.5 Every team accepts the official schedule of the competition and declares its ability to be available during these times.</br>

1.3.6 Every team and participant has to try to win every game at every stage of the competition. Purposefully losing for any reason is strictly forbidden.</br>
</p>
<p>
<h2>2. Teams and Players</h2>

<h4>2.1 Team Size</h4>

A Team has to consist of five participants. Those persons will be called "team members" or "members" of the team in the tourney.</br>

<h4>2.2 Registration</h4>

2.2.1 In order to sign up for the competition, a team has to fulfill all requirements the tournament platform forces.</br>

<h4>2.3 Lineup Changes</h4>

2.3.1 Team members can be removed or added during the competition as long as the minimum team requirements are met at all times. There might be additional restrictions to this rule in specific competitions.</br>

2.3.2 If another team member than before should be declared leader of a team, this has to either be set by the old leader himself in the team managment, or be requested by at least four of the team members.</br>

<h4>2.4 Stand-ins</h4>

2.4.1 Besides the official members a team has the right to use another person as a player during a match, if this person meets the following requirement: He is not member of another team or has played a game for another team within the competition. This person will be called "stand-in" in the tourney.</br>

</p>
<p>
<h2>3. Schedule</h2>

<h4>3.1 Match times</h4>

3.1.1 The official schedule of the competition shall be updated in the website once the registration has closed. This schedule includes the time of each match or of each round (in which case all matches of the round have that time as official match time).</br>

3.1.2 If a delayed end of a previous match prohibits a match to start on schedule (due to either one of the involved teams still playing or the official broadcaster still streaming the previous match), the match time is changed to 15 minutes after the previous match ends.</br>

<h4>3.2 Rescheduling</h4>

The change of a match time is only possible under exceptional circumstances and has to be confirmed by the administration.</br>

<h4>3.3 Punctuality</h4>

3.3.1 If one team has not shown any sign of activity (that includes: No team member wrote a match comment, no team member has joined the lobby, all other attempts to contact the team failed) until 15 minutes after the match time, a default win should be requested by the other team. The default win for such games will be confirmed by an admin under normal circumstances.</br>

3.3.2 If one team is not ready to start the match (but has shown activity) until 30 minutes after the match time, a default win should be requested by the other team. The default win for such games will be confirmed by an admin under normal circumstances.</br>

3.3.2 If both the teams are not ready by 15 minutes after the match time, the admins have the authority to declare both teams to have lost the match.</br>
</p>
<p>
<h2>4. Hosting and Settings</h2>

<h4>4.1 Lobby Hosting</h4>

4.1.1 An admin will create a lobby for each match 10 minutes before match time.</br>

4.1.2 If the lobby is not created at match time, the team that is listed first on the match schedule is expected to host the lobby and send the password directly to the other team.</br>

4.1.3 If the lobby is not created 10 minutes after match time, the team that is listed second on the match schedule is expected to host the lobby and send the password directly to the other team.</br>

4.1.4 In case of one of the teams creating the lobby, they are required to inform the administration before the match starts.</br>

<h4>4.2 Spectators</h4>

4.2.1 Every person is allowed to join the game as a broadcaster or unassigned player, if both teams allow it. If one team asks a spectator to leave before or during the game, he has to do that immediately.</br>

4.2.2 Official broadcasters and anyone authorized by a admin, has the right to spectate any game without the agreement of the teams. </br>

<h4>4.3 Sides and Drafting Order</h4>

The side and drafting order of each game depends on the mode of the match. The "first team" means the team mentioned first on the schedule page. The sides and drafting order of games within a type of match not mentioned in the following (e.g. best of five) are decided by a admin.</br>

4.3.1 One game: The first team chooses the side, the second team the drafting order.</br>

4.3.2 Two game series: The first team is radiant in game 1, drafting order is random. Game 2 is reverse.</br>

4.3.3 Best of three (or three games series): The first team is radiant in game 1, drafting order is random. Game 2 is reverse. In game 3, the first team chooses the side, the second team the drafting order.</br>

<h4>4.4 Game Settings</h4>

<b>GAME MODE </b>- Captains Mode</br>
<b>LOBBY PASSWORD </b>- see 4.1</br>
<b>STARTING TEAM </b>- see 4.3</br>
<b>PENALTY-RADIANT </b>- None, unless stated otherwise by an admin</br>
<b>PENALTY-DIRE </b>- None, unless stated otherwise by an admin</br>
<b>SERIES TYPE </b>- free choice, recommended to fit the match mode</br>
<b>Enable cheats </b>- No</br>
<b>Fill empty slots with bots </b>- No</br>
<b>All Chat </b>- Yes</br>
<b>BOT DIFFICULTY </b>- free choice</br>
<b>VERSION </b>- Tournament</br>
<b>SPECTATORS </b>- Enabled</br>
<b>DOTA TV DELAY </b>- 2 minutes</br>

4.4.1 In case of one of the teams starting the match, they will have to use these settings. Any match played with different settings than the above mentioned, will be voided, unless it was validated by an admin earlier.</br>
</p>
<p>
<h2>5. During a game</h2>

<h4>5.1 Picks and Bans</h4>

The hero pool always consists of all heroes the game mode allows and might therefore change several times during the competition. The game must not be paused during the draft, unless a drafter disconnects.</br>

<h4>5.2 Pauses</h4>

Each team has the right to pause the game for an acceptable duration, if a player announces it at least three seconds in advance and calls a coherent reason (exception: not necessary, if someone disconnects). The game must not be resumed before both teams agree with it. The maximum pause time alowed for a team is 15 minutes. When the number or the duration of breaks becomes too high or long, the administration may force the continuation of the game. </br>

<h4>5.3 Disconnects and Reconnects</h4>

If a player disconnects, the game is to be paused instantly. Everyone has up to ten minutes to reconnect to the game (if the same person disconnects more than once, all times get added up). If this time is passed, the opposing team may continue the game, unless an admin demands the opposite. The affected player is in this case still allowed to rejoin later on.</br>

<h4>5.4 Broadcasting</h4>

5.4.1 Every authorized spectator (according to 4.2) inside the game is allowed to provide a video stream with a delay of at least two minutes.</br>

5.4.2 The players are not allowed to watch or listen to any of these video streams at any time during their match. Furthermore they are not allowed to provide a stream themselves, unless approved by an admin.</br>

<h4>5.5 Bugs</h4>

If any serious bugs occur, the game has to be paused immediately and the administration decides how to continue. Abusing a bug is forbidden.</br>
<h4>5.6 Tools / Hacks</h4>

The usage of any Dota-related tool aside from Steam and Dota 2 during a match is strictly forbidden.</br>

<h4>5.7 Ending</h4>

5.7.1 A game is finished, when an Ancient is destroyed, when the majority of a team leaves on purpose, or when the administration decides on it. (note: A game is not forfeited, when a player types "gg" or "ff". Always wait for the ancient to be destroyed!)</br>

5.7.2 If an admin/refree is not spectatig the match, both the teams are expected to submit a screenshot of the match result to the administration as soon as the match is finished.</br>
</p>
<p>
<h2>6. Rule Violation and Punishment</h2>

<h4>6.1 Breaking of the rules or ignoring the order of an admin may result in a punishment for a whole team or a specific player. Type and amount is chosen by a admin.</h4>

<h4>6.2. Possible Team Punishments</h4>
Default Loss for one game</br>
Default Loss for the whole match</br>
Time Penalty for the next draft</br>
Score Reduction (during a group stage)</br>
Temporary or permanent ban from the competition</br>

<h4>6.3. Possible Player Punishments</h4>
Temporary ban from the competition
Permanent ban from the competition

<h4>6.4 In case one of the players is banned, the team may continue in the tournament if they have registered a standby.</h4>

<h4>6.5 If any team has less than 5 unbanned members at any time in the tournament, the admins have the right to disqualify them immediately.</h4>
</p>
<p>
<h2>7. Prize Money Distribution</h2>

The prize money will only be transferred to the team leader. The team leader has to make sure every member of his team will get his adequate percentage of the prize money. For more details, contact the administration.
</p>                        
